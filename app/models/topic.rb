##
# This class represents a Topic.
# Ex. ["Integers", "Fractions"]
class Topic < ActiveRecord::Base
  
    ############################################################
  # Basic setup
  ############################################################
  
  # ----------------------------------------------------------
  # Attributes
  
  attr_accessible :name, :section_type_id
  
  # ----------------------------------------------------------
  # Validations
  
  validates :name, :slug, :presence => true
  validates :name, :slug, :uniqueness => true
  
  # ----------------------------------------------------------
  # Before-After Callbacks
  
  before_validation :generate_slug
  
  ############################################################
  # Relations
  ############################################################
  
  # ----------------------------------------------------------
  # belongs_to
  
  belongs_to :section_type
  
  # ----------------------------------------------------------
  # has_many
  
  has_many :quizzes
  
  # ----------------------------------------------------------
  # has_many :through
  
  ############################################################
  # Scopes
  ############################################################
  
  # ----------------------------------------------------------
  # Direct scopes
  
  # ----------------------------------------------------------
  # Lambda scopes
  
  ###########################################################
  # Functions
  ############################################################
  
  # ----------------------------------------------------------
  # Overrides
  
  # ----------------------------------------------------------
  # Instance methods
 
  # ----------------------------------------------------------
  # Class methods
  
  def to_param
    slug
  end
  
  private
  
  def generate_slug
    self.slug = name.parameterize
  end
  
end
